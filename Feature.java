package com.samissua.gym_membership;

/**
 *
 * @author CltControl
 */
public class Feature {
    private String name;
    private Float cost;
    
    public Feature(String name, Float cost) {
        this.name = name;
        this.cost = cost;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Float getCost() {
        return cost;
    }

    public void setCost(Float cost) {
        this.cost = cost;
    }
}
